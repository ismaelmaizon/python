#               Listas y tipos de listas:

#En Python, una lista es una colección ordenada y mutable de elementos. 
#Puedes crear una lista utilizando corchetes [] y separando los elementos con comas. 
#Python también ofrece otros tipos de listas como tuplas (inmutables) y 
#conjuntos (colecciones no ordenadas de elementos únicos).

