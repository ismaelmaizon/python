#               Tuplas
# En Python, una tupla es una colección ordenada e inmutable de elementos. 
# Se utiliza para almacenar múltiples elementos en una sola variable. 
# Las tuplas son similares a las listas, pero a diferencia de las listas, las tuplas no se pueden modificar una vez 
# creadas, lo que significa que no se pueden agregar, eliminar o modificar elementos individualmente.


tupla = (1, 2, 3, 4, 5)